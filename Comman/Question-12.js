//: Is JavaScript a compiled or interpreted language

/* JavaScript is an interpreted language, not a compiled language. An interpreter in the browser reads over the JavaScript code, interprets each line, and runs it. Nowadays mordern browsers use a technology known as Just-In-Time (JIT) compilation, which compiles JavaScript to executable bytecode just as it is about to run. */

//: is JavaScript a case-sensitive language

/* Yes, JavaScript is a case sensitive language. The language keywords, variables, functions & object names, and any other identifiers must always be typed with a consistent capitalization of letters. */

//: is there any relation between Java and JavaScript

/* No, they are entirely two different programmning languages and have nothing to do with each ohter. But both of them are Object Oriented Programming languages and like many other language, they follow similar syntax for basic features (if, else, for, switch, break, continue etc). */

//: What is same-origin policy

/* The same-origin policy is a policy that prevents JavaScript from making requests across domain boundaries. An origin is defined as a combination of URI scheme, hostname, and port number. If you enable this policy then t prevents a malicious script on one page from obtaining access to sensitive data on another web page using Document Object Model(DOM). */

